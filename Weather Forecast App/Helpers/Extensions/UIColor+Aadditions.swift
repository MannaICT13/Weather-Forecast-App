import UIKit

extension UIColor {
    static var sunnyBackgroundColor: UIColor {
        return UIColor(red: 1.0, green: 0.8, blue: 0.0, alpha: 1.0)
    }
    
    static var rainyBackgroundColor: UIColor {
        return UIColor(red: 0.2, green: 0.4, blue: 0.8, alpha: 1.0)
    }
    
    static var cloudyBackgroundColor: UIColor {
        return UIColor(red: 0.5, green: 0.5, blue: 0.5, alpha: 1.0)
    }
}
